      real*8 function media(c,b)
      implicit none
      real*8 b,c
      media = c*gamma(1.d0+1.d0/b)
      return
      end

      real*8 function desv(c,b)
      implicit none
      real*8 mu,b,c,media
      mu =  media(c,b)
      desv = dsqrt((c**2)*gamma(1.d0+2.d0/b) - mu**2)
      return
      end