/********************************************************************** 
 * Data de criacao    : 21/03/2020                                    *
 * Data de modificaco : 00/00/0000                                    * 
 * ------------------------------------------------------------------ *   
 * Subroutine weibull : calculo de b e c apartir de mu e sig          *    
 * ------------------------------------------------------------------ * 
 * Parametros de entrada:                                             *
 * ------------------------------------------------------------------ * 
 * mu    - media                                                      *
 * sig   - desvio padrão                                              *
 * b     - nao definido                                               *
 * c     - nao definido                                               *
 * tol   - tolerancia do residuo                                      *
 * maxIt - numero maximo de iteracaos                                 *
 * ------------------------------------------------------------------ * 
 * Parametros de saida:                                               *
 * ------------------------------------------------------------------ *
 * b - valor de b para mu e sig                                       *
 * c - valor de c para mu e sig                                       *
 * ------------------------------------------------------------------ * 
 * OBS:                                                               *
 * ------------------------------------------------------------------ *
 *        | z*gamma(z)     0.0         0.0   |                        *  
 * A(x) = |        0.0 c^2*gamma(w)    0.0   |                        *
 *        |        0.0     1.0        -2.0   |                        *
 *                                                                    *
 *     | c |       |     mu       |                                   *
 * x = | w |   f = | sig^2 + mu^2 |                                   *
 *     | z |       |     0.0      |                                   *
 *                                                                    *
 * ------------------------------------------------------------------ * 
 **********************************************************************/
void weibull(double const mu , double const sig
            ,double *b       , double *c
            ,double const tol, int const maxIt)
{
  short fFag;
  int i, j;
  double alf, x[3], x0[3], a[4], f[2], res;

/*...*/
  fFag = 1;
  alf  = 0.5e0;
/*...................................................................*/

/*... chute inicial*/
  *c = mu;
  *b = sig/mu;
  x[0] = *c;
  x[1] = 2.e0/(*b);
  x[2] = 1.e0/(*b);
/*...................................................................*/

/*...*/
  f[0] = mu;
  f[1] = sig*sig + mu*mu;
/*...................................................................*/

/*...*/
  for(i = 0; i < maxIt; i++)  {
/*...*/
    for(j = 0; j < 3; j++)
      x0[j] = x[j];
/*...................................................................*/
  
/*... atualizando a matriz a*/
    updateA(a, x);
/*...................................................................*/

/*...*/
    res = modRes(f, a, x);
    if( res < tol)  {
      fFag = 0;
      break;
    }
/*...................................................................*/

/*... x = (A^-1)*F*/
    solvA(a, x, f);
/*...................................................................*/

/*... relaxamento*/
    for(j = 0; j < 3; j++)
      x[j] = alf*x[j] + (1.e0 - alf)*x0[j];
/*...................................................................*/
  }
/*...................................................................*/

/*...*/
  if(fFag) 
    printf("!!!Warning!!!\n");
/*...................................................................*/

/*...*/
  printf("*********************************************************\n");
  printf("Res final %e para it %d\n", res, i);
  printf("*********************************************************\n");
/*...................................................................*/

/*...*/
  *c = x[0];
  *b = 2.e0/x[1];
/*...................................................................*/

}
