/**********************************************************************
 * Data de criacao    : 08/03/2020                                    *
 * Data de modificaco : 00/00/0000                                    * 
 * ------------------------------------------------------------------ *   
 * Subroutine updateA : atualizacao da matriz a com os valores x      *    
 * ------------------------------------------------------------------ * 
 * Parametros de entrada:                                             *
 * ------------------------------------------------------------------ * 
 * a - matriz 3x3 em funcao de x                                      *
 * x - valores de x                                                   *
 * ------------------------------------------------------------------ * 
 * Parametros de saida:                                               *
 * ------------------------------------------------------------------ *
 * a - matriz atualizada                                              *
 * ------------------------------------------------------------------ * 
 * OBS:                                                               *
 * ------------------------------------------------------------------ *
 *        | z*gamma(z)     0.0         0.0   |                        *  
 * A(x) = |        0.0 c^2*gamma(w)    0.0   |                        *
 *        |        0.0    -2.0         1.0   |                        *
 *                                                                    *
 *     | c |                                                          *
 * x = | w |                                                          *
 *     | z |                                                          *
 *                                                                    *
 * ------------------------------------------------------------------ * 
 **********************************************************************/
void updateA( double *a, double *x)
{
  a[0] = x[2]*tgamma(x[2]);
  a[1] = x[0]*x[0]*tgamma(x[1]);
  a[2] =  2.e0;
  a[3] = -1.e0;
}
/**********************************************************************/

/**********************************************************************
 * Data de criacao    : 21/03/2020                                    *
 * Data de modificaco : 00/00/0000                                    * 
 * ------------------------------------------------------------------ *   
 * Subroutine solA    : modulo do residuo                             *    
 * ------------------------------------------------------------------ * 
 * Parametros de entrada:                                             *
 * ------------------------------------------------------------------ * 
 * ------------------------------------------------------------------ * 
 * Parametros de saida:                                               *
 * ------------------------------------------------------------------ *
 * ------------------------------------------------------------------ * 
 * OBS:                                                               *
 * ------------------------------------------------------------------ *
 * ------------------------------------------------------------------ * 
 **********************************************************************/
void solvA( double *a, double *x, double *f)
{
  x[0] = f[0]/a[0];
  x[1] = f[1]/a[1];
  x[2] = -a[3]*x[1]/a[2];
}
/**********************************************************************/

/**********************************************************************
 * Data de criacao    : 21/03/2020                                    *
 * Data de modificaco : 00/00/0000                                    * 
 * ------------------------------------------------------------------ *   
 * Subroutine modRes  : modulo do residuo                             *    
 * ------------------------------------------------------------------ * 
 * Parametros de entrada:                                             *
 * ------------------------------------------------------------------ * 
 * ------------------------------------------------------------------ * 
 * Parametros de saida:                                               *
 * ------------------------------------------------------------------ *
 * ------------------------------------------------------------------ * 
 * OBS:                                                               *
 * ------------------------------------------------------------------ *
 * ------------------------------------------------------------------ * 
 **********************************************************************/
double modRes( double *f, double *a, double *x)
{
  double r[3];
/*... R = F - Ax*/
  r[0] = f[0] - a[0]*x[0];
  r[1] = f[1] - a[1]*x[1];
  r[2] = - a[2]*x[2] - a[3]*x[1];
/*...................................................................*/

/*... |R|*/
  return sqrt( r[0]*r[0] + r[1]*r[1] + r[2]*r[2]);

}
